def det3(m):
    a = m[0][0]*m[1][1]*m[2][2]
    b = m[0][1]*m[1][2]*m[2][0]
    c = m[0][2]*m[1][0]*m[2][1]
    d = m[0][2]*m[1][1]*m[2][0]
    e = m[0][0]*m[1][2]*m[2][1]
    f = m[0][1]*m[1][0]*m[2][2]
    return a+b+c-d-e-f

def det4(m):
    a = m[0][0]
    b = m[0][1]
    c = m[0][2]
    d = m[0][3]
    m1 = det3([[m[1][1],m[1][2],m[1][3]],[m[2][1],m[2][2],m[2][3]]\
               ,[m[3][1],m[3][2],m[3][3]]])
    m2 = det3([[m[1][0],m[1][2],m[1][3]],[m[2][0],m[2][2],m[2][3]],\
               [m[3][0],m[3][2],m[3][3]]])
    m3 = det3([[m[1][0],m[1][1],m[1][3]],[m[2][0],m[2][1],m[2][3]],\
               [m[3][0],m[3][1],m[3][3]]])
    m4 = det3([[m[1][0],m[1][1],m[1][2]],[m[2][0],m[2][1],m[2][2]],\
               [m[3][0],m[3][1],m[3][2]]])
    return a*m1-b*m2+c*m3-d*m4


